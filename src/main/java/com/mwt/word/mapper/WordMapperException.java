package com.mwt.word.mapper;

public class WordMapperException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public WordMapperException(String message) {
		super(message);
	}
	
	public WordMapperException(Throwable cause) {
		super(cause);
	}
	
	public WordMapperException(String message, Throwable cause) {
		super(message, cause);
	}

	
}
